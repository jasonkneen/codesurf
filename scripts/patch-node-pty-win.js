/**
 * Patches node-pty's native build files for Windows compatibility.
 *
 * Fixes three issues:
 * 1. winpty.gyp uses `cmd /c "cd shared && GetCommitHash.bat"` which fails
 *    because gyp runs from the wrong working directory. We replace the shell
 *    command with a static value.
 * 2. Both binding.gyp and winpty.gyp require Spectre-mitigated libraries
 *    which most dev machines don't have installed. We disable the requirement.
 * 3. winpty.gyp calls UpdateGenVersion.bat which also fails from the wrong
 *    cwd. We pre-create the generated header and point the include to it.
 *
 * Exported so the before-build hook can invoke it via function call — a
 * top-level `process.exit` would terminate electron-builder itself.
 * Also callable directly via `node scripts/patch-node-pty-win.js` (postinstall).
 *
 * Each regex replacement is verified: if the source pattern didn't match AND
 * the patched marker isn't already present, we throw. That way an upstream
 * rename in node-pty fails the build loudly rather than silently producing a
 * broken binary.
 */

const fs = require('fs')
const path = require('path')

/**
 * Run `content.replace(pattern, replacement)` and verify the result either
 * changed or already contained `expectedMarker` (so repeat runs are idempotent).
 * Throws if neither condition holds — means node-pty upstream changed shape
 * and the patch silently became a no-op.
 */
function assertReplace(content, pattern, replacement, expectedMarker, label) {
  const next = content.replace(pattern, replacement)
  if (next === content && !content.includes(expectedMarker)) {
    throw new Error(
      `[patch-node-pty-win] ${label}: neither source pattern nor expected marker found. ` +
      `node-pty's build files likely changed shape — update the patch script.`
    )
  }
  return next
}

function patchNodePtyWin() {
  if (process.platform !== 'win32') {
    console.log('[patch-node-pty-win] Not on Windows, skipping.')
    return
  }

  const nodePtyDir = path.join(__dirname, '..', 'node_modules', 'node-pty')
  if (!fs.existsSync(nodePtyDir)) {
    console.log('[patch-node-pty-win] node-pty not found, skipping.')
    return
  }

  // --- Patch winpty.gyp ---
  const winptyGyp = path.join(nodePtyDir, 'deps', 'winpty', 'src', 'winpty.gyp')
  if (fs.existsSync(winptyGyp)) {
    let content = fs.readFileSync(winptyGyp, 'utf8')

    content = assertReplace(
      content,
      /'WINPTY_COMMIT_HASH%':\s*'<!\(cmd \/c "cd shared && GetCommitHash\.bat"\)'/,
      "'WINPTY_COMMIT_HASH%': 'none'",
      "'WINPTY_COMMIT_HASH%': 'none'",
      'GetCommitHash.bat patch',
    )

    content = assertReplace(
      content,
      /'<!\(cmd \/c "cd shared && UpdateGenVersion\.bat <\(WINPTY_COMMIT_HASH\)"\)'/,
      "'gen'",
      "'gen'",
      'UpdateGenVersion.bat patch',
    )

    // Spectre mitigation isn't a single-shot — the token may appear many times.
    // Idempotent check: after replace, the original token must be gone.
    content = content.replace(/'SpectreMitigation': 'Spectre'/g, "'SpectreMitigation': 'false'")
    if (content.includes("'SpectreMitigation': 'Spectre'")) {
      throw new Error('[patch-node-pty-win] winpty.gyp: Spectre mitigation disable did not fully apply')
    }

    fs.writeFileSync(winptyGyp, content)
    console.log('[patch-node-pty-win] Patched winpty.gyp')

    // Pre-create the GenVersion.h header
    const versionFile = path.join(nodePtyDir, 'deps', 'winpty', 'VERSION.txt')
    const version = fs.existsSync(versionFile)
      ? fs.readFileSync(versionFile, 'utf8').trim()
      : '0.0.0'
    const genDir = path.join(nodePtyDir, 'deps', 'winpty', 'src', 'gen')
    fs.mkdirSync(genDir, { recursive: true })
    fs.writeFileSync(
      path.join(genDir, 'GenVersion.h'),
      `// AUTO-GENERATED BY patch-node-pty-win.js\nconst char GenVersion_Version[] = "${version}";\nconst char GenVersion_Commit[] = "none";\n`,
    )
    console.log('[patch-node-pty-win] Created GenVersion.h')
  } else {
    throw new Error(
      `[patch-node-pty-win] ${winptyGyp} not found — node-pty's winpty directory layout changed, update the patch script.`,
    )
  }

  // --- Patch binding.gyp ---
  const bindingGyp = path.join(nodePtyDir, 'binding.gyp')
  if (fs.existsSync(bindingGyp)) {
    let content = fs.readFileSync(bindingGyp, 'utf8')
    content = content.replace(/'SpectreMitigation': 'Spectre'/g, "'SpectreMitigation': 'false'")
    if (content.includes("'SpectreMitigation': 'Spectre'")) {
      throw new Error('[patch-node-pty-win] binding.gyp: Spectre mitigation disable did not fully apply')
    }
    fs.writeFileSync(bindingGyp, content)
    console.log('[patch-node-pty-win] Patched binding.gyp (disabled Spectre mitigation)')
  }

  console.log('[patch-node-pty-win] Done.')
}

module.exports = { patchNodePtyWin }

// Run directly (via postinstall): `node scripts/patch-node-pty-win.js`
if (require.main === module) {
  patchNodePtyWin()
}
